# coding:utf-8
# 微秒
MICROSECOND = 0.000001
# 毫秒
MILLISECOND = MICROSECOND * 1000
# 秒 这里秒作为基础单位
SECOND = MILLISECOND * 1000
# 分
MINUTE = SECOND * 60
# 时
HOUR = MINUTE * 60
# 天
DAY = HOUR * 24
# 昨天
YESTERDAY = DAY * 2
# 前天
BEFORE_DAY = DAY * 3
# 星期
WEEK = DAY * 7
# 月
MONTH = DAY * 30
# 年
YEAR = DAY * 365
